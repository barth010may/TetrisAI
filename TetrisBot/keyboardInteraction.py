import pyautogui
import time

class KeyboardInput:

    def press_space(self):
        # Press Space
        pyautogui.press('space')
        time.sleep(0.001)  # Short delay between key presses

    def press_right(self):
        # Press Space
        pyautogui.press('right')
        time.sleep(0.1)  # Short delay between key presses

    def press_left(self):
        # Press Space
        pyautogui.press('left')
        time.sleep(0.1)  # Short delay between key presses

    def press_up(self):
        # Press Space
        pyautogui.press('up')
        time.sleep(0.1)  # Short delay between key presses
